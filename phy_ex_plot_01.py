import sympy as sym  # 代数計算のためのライブラリをimport
t, g, v0, theta = sym.symbols('t,g,v0,theta')  # 変数を宣言
y1 = 100*(1/1.414)*t-9.8*t*t #式1(抵抗なし)を定義
y2 = -9.8*10*t/3.4 + (-9.8*10**2 - 3.4*10*100*1/1.414) * sym.exp(-3.4*t/10)/3.4**2 + (9.8*10**2 + 3.4*10*100*1/1.414)/3.4**2 #式2(抵抗あり)を定義
sym.plot(y1, y2, (t, 0, 10)) #式1、式2のグラフをプロット
